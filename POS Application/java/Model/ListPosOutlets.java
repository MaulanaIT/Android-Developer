package com.example.project.Model;

public class ListPosOutlets {

    public String
            outletsName,
            outletsAddress,
            outletsTelephone;

    int
            outletID;

    public int getOutletID() {
        return outletID;
    }

    public void setOutletID(int outletID) {
        this.outletID = outletID;
    }

    public String getOutletsName() {
        return outletsName;
    }

    public void setOutletsName(String outletsName) {
        this.outletsName = outletsName;
    }

    public String getOutletsAddress() {
        return outletsAddress;
    }

    public void setOutletsAddress(String outletsAddress) {
        this.outletsAddress = outletsAddress;
    }

    public String getOutletsTelephone() {
        return outletsTelephone;
    }

    public void setOutletsTelephone(String outletsTelephone) {
        this.outletsTelephone = outletsTelephone;
    }
}
